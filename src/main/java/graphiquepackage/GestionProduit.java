    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        
        int id_produit = Integer.parseInt(jT_idproduit.getText());
        
        System.out.println(id_produit);
        
        DefaultTableModel model = (DefaultTableModel) jT_produit.getModel();
        
        if(jT_produit.getSelectedRow() == -1){
            if(jT_produit.getRowCount() == 0){
                message.setText("La facture est encore vide !");
            }
            else{
                message.setText("Vous devriez d'abord selectionner un article de la facture");
            }
        }
        
        else{
            
            int j = jT_produit.getSelectedRow(); 
            int i = jT_produit.convertRowIndexToModel(j);
            
            
            Miniprojetjava.baseDeDonnees.supprimerProduit_BD(id_produit);
            model.removeRow(i);
                    
            jT_idproduit.setText("0");
            jT_nom.setText("");
            jT_description.setText("");
            jT_prix.setText("0");
            jT_paysorigine.setText("");
            jT_tva.setText("0");
            jT_fournisseur.setText("");
            jT_poids.setText("0");
            jT_quantitestock.setText("0");

            message.setText("Cliquez sur le bouton ajouter pour ajouter un produit à la facture");
            
        }
        
    }//GEN-LAST:event_supprimerActionPerformed

