    private void ajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterActionPerformed
        
        
        int id_produit = 0 ;
        double prix = 0 ;
        double tva = 0 ;
        double poids = 0 ;
        double quantite_stock = 0 ;
        
        if(jT_nom.getText().equals("")  || jT_description.getText().equals("")  || jT_prix.getText().equals("") || jT_paysorigine.getText().equals("") || jT_tva.getText().equals("") || jT_fournisseur.getText().equals("") || jT_poids.getText().equals("") || jT_quantitestock.getText().equals("")){
            message.setText("Tous les champs doivent être remplis");
        }
        else{
            try{
                prix = Double.parseDouble(jT_prix.getText());
                tva = Double.parseDouble(jT_tva.getText());
                poids = Double.parseDouble(jT_poids.getText());
                quantite_stock = Double.parseDouble(jT_quantitestock.getText());
                
                int option = JOptionPane.showOptionDialog(
                    null,                             // Parent component
                    "Voulez-vous vraiment l'ajouter comme un nouveau produit ?",       // Message
                    "Confirmation",                  // Titre de la boîte de dialogue
                    JOptionPane.YES_NO_OPTION,        // Type de boîte de dialogue
                    JOptionPane.QUESTION_MESSAGE,    // Type d'icône
                    null,                             // Options personnalisées
                    null,                             // Option sélectionnée par défaut
                    null                              // Options de fermeture de la boîte de dialogue
                );

                if (option == JOptionPane.YES_OPTION) {
    

                    id_produit = Miniprojetjava.baseDeDonnees.dernier_id_produit + 1 ;
                
                    Produit p = new Produit( id_produit +"_"+ jT_nom.getText() + "_" + jT_description.getText() + "_" + jT_prix.getText() + "_" + jT_paysorigine.getText() + "_" + jT_tva.getText() + "_" + jT_fournisseur.getText() + "_" + jT_poids.getText() + "_" + jT_quantitestock.getText() + "_0");

                    DefaultTableModel model = (DefaultTableModel) jT_produit.getModel();
                    model.addRow(new Object[]{p.idproduit, p.nom, p.description, p.prix , p.paysorigine, p.tva, p.fournisseur, p.poids, p.quatitestock});
                    
                    Miniprojetjava.baseDeDonnees.ajouterProduit_BD(p);

                    jT_idproduit.setText("0");
                    jT_nom.setText("");
                    jT_description.setText("");
                    jT_prix.setText("0");
                    jT_paysorigine.setText("");
                    jT_tva.setText("0");
                    jT_fournisseur.setText("");
                    jT_poids.setText("0");
                    jT_quantitestock.setText("0");


                    message.setText("Vous pouvez cliquez sur les entetes des colonnes, pour trier la table suivant cette colonne");
                }

                else{
                    message.setText("Vous pouvez cliquez sur les entetes des colonnes, pour trier la table suivant cette colonne");
                }

            }

            catch(Exception e){
                message.setText("Le prix, tva, poids, qutite stock doivent être des nombres");
            }
        }
        
        
    }//GEN-LAST:event_ajouterActionPerformed

