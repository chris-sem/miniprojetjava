    private void boutonAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boutonAjouterActionPerformed
        
        double quantite ;
        double remise ;
        double prix ;
        
        try{
            quantite = Double.parseDouble(jT_qtitepoids.getText());
            remise = Double.parseDouble(jT_remise.getText());
            
            
            if(jT_produit.getText().equals("")){
                
                message.setText("Aucun produit n'a encore été selectioné");
                
            }
            else{
                
                if(quantite != 0){
                    prix = Double.parseDouble(jT_prix.getText());
                    DefaultTableModel model = (DefaultTableModel) facture.getModel();
                    model.addRow(new Object[]{jT_id_produit.getText(), jT_produit.getText(), quantite , jC_type.getSelectedItem().toString() , (prix - ((prix*remise)/100))*quantite});
                    
                    jT_id_produit.setText("");
                    jT_produit.setText("");
                    jT_prix.setText("");
                    jT_qtitepoids.setText("0");
                    jT_remise.setText("0");
                    
                    calculTotalFacture(facture, jL_total);
                    
                    jC_type.setSelectedItem("autre");
                    
                    message.setText("Cliquez sur le bouton ajouter pour ajouter un produit à la facture");

                }
                else{
                    message.setText("La quantité doit être different 0");
                }
                
            }
            
        }
        
        catch(Exception e){
            message.setText("Verifiez la valeur de la quantité ou de la remise");
        }
        
        
        
    }//GEN-LAST:event_boutonAjouterActionPerformed

