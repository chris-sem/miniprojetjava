    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
 
        
        if((entree.length() != 0) && (operateur.length() != 0)){
            
            
                try{
                    operande2 = Double.parseDouble(entree);

                    if(operateur.equals("+")){
                        entree = "";
                        entree = String.format("%.3f", (operande1 + operande2)) ;
                        operateur = "";
                        jL_operateur.setText(operateur);
                        jL_entree.setText(entree);
                    }
                    
                    if(operateur.equals("-")){
                        entree = "";
                        entree = String.format("%.3f", (operande1 - operande2)) ;
                        operateur = "";
                        jL_operateur.setText(operateur);
                        jL_entree.setText(entree);
                    }
                    
                    if(operateur.equals("*")){
                        entree = "";
                        entree = String.format("%.3f", (operande1 * operande2)) ;
                        operateur = "";
                        jL_operateur.setText(operateur);
                        jL_entree.setText(entree);
                    }
                    
                    if(operateur.equals("/")){
                        
                        if(operande2 == 0){
                            operande1 = 0.0 ;
                            operande2 = 0.0 ;
                            resultat = 0.0 ;
                            entree = "" ;
                            operateur = "";
                            
                            operateur = "";
                            jL_operateur.setText(operateur);

                            jL_entree.setText("Erreur");
                        }
                        else{
                            entree = "";
                            entree = String.format("%.3f", (operande1 / operande2)) ;
                            operateur = "";
                            jL_operateur.setText(operateur);
                            jL_entree.setText(entree);
                        }
                    }
                    
                }
                catch(Exception e){
                    operande1 = 0.0 ;
                    operande2 = 0.0 ;
                    resultat = 0.0 ;
                    entree = "" ;
                    operateur = "";

                    jL_operateur.setText("");
                    jL_entree.setText("Erreur");
                }
            

            
        }
    }//GEN-LAST:event_jButton18ActionPerformed

