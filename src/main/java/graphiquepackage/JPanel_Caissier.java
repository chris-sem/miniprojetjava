    private void BoutonmisejourActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BoutonmisejourActionPerformed
        
        DefaultTableModel model = (DefaultTableModel) facture.getModel();
        
        if(facture.getSelectedRow() == -1){
            if(facture.getRowCount() == 0){
                message.setText("La facture est encore vide !");
            }
            else{
                message.setText("Vous devriez d'abord selectionner un article de la facture");
            }
        }
        
        else{
            
            double quantite ;
            double remise ;
            double prix ;

            try{
                quantite = Double.parseDouble(jT_qtitepoids.getText());
                remise = Double.parseDouble(jT_remise.getText());


                if(jT_produit.getText().equals("")){

                    message.setText("Aucun produit n'a encore été selectioné");

                }
                else{

                    if(quantite != 0){
                        
                        prix = Double.parseDouble(jT_prix.getText());
                        
                        model.setValueAt(jT_id_produit.getText(), facture.getSelectedRow(), 0);
                        model.setValueAt(jT_produit.getText(), facture.getSelectedRow(), 1);
                        model.setValueAt(quantite, facture.getSelectedRow(), 2);
                        model.setValueAt(jC_type.getSelectedItem().toString(), facture.getSelectedRow(), 3);
                        model.setValueAt((prix - ((prix*remise)/100))*quantite, facture.getSelectedRow(), 4);
                        
                        jT_id_produit.setText("");
                        jT_produit.setText("");
                        jT_prix.setText("");
                        jT_qtitepoids.setText("0");
                        jT_remise.setText("0");
                        
                        calculTotalFacture(facture, jL_total);
                        
                        jC_type.setSelectedItem("autre");
                        
                        message.setText("Cliquez sur le bouton ajouter pour ajouter un produit à la facture");

                    }
                    else{
                        message.setText("La quantité doit être different 0");
                    }

                }

            }

            catch(Exception e){
                message.setText("Verifiez la valeur de la quantité ou de la remise");
            }
        }
        
        
    }//GEN-LAST:event_BoutonmisejourActionPerformed

