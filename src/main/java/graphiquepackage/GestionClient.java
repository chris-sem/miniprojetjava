                    model.setValueAt(p.idclient, jT_clients.getSelectedRow(), 0);
                    model.setValueAt(p.nom, jT_clients.getSelectedRow(), 1);
                    model.setValueAt(p.prenom, jT_clients.getSelectedRow(), 2);
                    model.setValueAt(p.numerocartefidelite, jT_clients.getSelectedRow(), 3);
                    model.setValueAt(p.mail, jT_clients.getSelectedRow(), 4);
                    model.setValueAt(p.codepostal, jT_clients.getSelectedRow(), 5);
                    
                    
                    
                    //model.addRow(new Object[]{p.idclient, p.nom, p.prenom, p.numerocartefidelite , p.mail, p.codepostal});
                    
                    Miniprojetjava.baseDeDonnees.modifierClient_BD(id_client, p);
                    
