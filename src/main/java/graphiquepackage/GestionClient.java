    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        
        int id_client = Integer.parseInt(jT_idclient.getText());
        
        System.out.println(id_client);
        
        DefaultTableModel model = (DefaultTableModel) jT_clients.getModel();
        
        if(jT_clients.getSelectedRow() == -1){
            if(jT_clients.getRowCount() == 0){
                message.setText("La table est encore vide !");
            }
            else{
                message.setText("Vous devriez d'abord selectionner un item de la table");
            }
        }
        
        else{
            
            int j = jT_clients.getSelectedRow(); 
            int i = jT_clients.convertRowIndexToModel(j);
            
            
            Miniprojetjava.baseDeDonnees.supprimerClient_BD(id_client);
            model.removeRow(i);
                    
            jT_idclient.setText("0");
            jT_nom.setText("");
            jT_prenom.setText("");
            jT_numcartefidelite.setText("");
            jT_mail.setText("");
            jT_codepostale.setText("");

            message.setText("Cliquez sur le bouton ajouter pour ajouter un client à la table");
            
        }
    }//GEN-LAST:event_supprimerActionPerformed

